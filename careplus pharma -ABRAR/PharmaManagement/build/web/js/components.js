// Create Stat Card
function createStatCard(title, value, iconName, trendValue, colorClass) {
    const icon = Icons[iconName] || Icons.package;
    const trendHtml = trendValue !== undefined ? `
    <div class="stat-card-trend ${trendValue > 0 ? 'up' : 'down'}">
      ${trendValue > 0 ? Icons.arrowUpRight : Icons.arrowDownRight}
      <span>${Math.abs(trendValue)}% vs last month</span>
    </div>
  ` : '';

    return `
    <div class="stat-card">
      <div class="stat-card-content">
        <div class="stat-card-info">
          <p class="stat-card-title">${title}</p>
          <h3 class="stat-card-value">${value}</h3>
          ${trendHtml}
        </div>
        <div class="stat-card-icon ${colorClass}">
          ${icon}
        </div>
      </div>
    </div>
  `;
}

// Initialize Page Layout
function initPageLayout(activePage) {
    requireAuth();

    const appWrapper = document.querySelector('.app-wrapper');
    if (appWrapper) {
        // Insert sidebar
        appWrapper.insertAdjacentHTML('afterbegin', createSidebar(activePage));

        // Insert header into main-content
        const mainContent = appWrapper.querySelector('.main-content');
        if (mainContent) {
            mainContent.insertAdjacentHTML('afterbegin', createHeader());
        }
    }

    // Add mobile backdrop
    document.body.insertAdjacentHTML('beforeend', '<div class="mobile-backdrop" id="mobileBackdrop" onclick="toggleSidebar()"></div>');
}

// Toggle Sidebar (Mobile)
function toggleSidebar() {
    const sidebar = document.getElementById('sidebar');
    const backdrop = document.getElementById('mobileBackdrop');

    if (sidebar && backdrop) {
        sidebar.classList.toggle('show');
        backdrop.classList.toggle('show');
    }
}

// Show Toast Notification
function showToast(message, type = 'success') {
    const toast = document.createElement('div');
    toast.style.cssText = `
    position: fixed;
    top: 20px;
    right: 20px;
    background: ${type === 'success' ? 'var(--emerald-600)' : 'var(--rose-600)'};
    color: white;
    padding: 1rem 1.5rem;
    border-radius: var(--radius-xl);
    box-shadow: var(--shadow-xl);
    z-index: 1000;
    font-weight: 600;
    animation: slideIn 0.3s ease-out;
  `;
    toast.textContent = message;
    document.body.appendChild(toast);

    setTimeout(() => {
        toast.style.animation = 'fadeOut 0.3s ease-out';
        setTimeout(() => toast.remove(), 300);
    }, 3000);
}

// Format Currency
function formatCurrency(amount) {
    return `$${amount.toFixed(2)}`;
}

// Format Date
function formatDate(dateString) {
    const date = new Date(dateString);
    return date.toLocaleDateString();
}

// Format DateTime
function formatDateTime(dateString) {
    const date = new Date(dateString);
    return date.toLocaleString();
}

// Truncate Text
function truncate(text, length = 20) {
    return text.length > length ? text.substring(0, length) + '...' : text;
}
